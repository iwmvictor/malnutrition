export function useTranslation(language: Language) {
  return {
    t: (key: string): string => {
      const keys = key.split(".");
      // eslint-disable-next-line @typescript-eslint/no-explicit-any
      let value: any = translation[language];

      for (const k of keys) {
        value = value?.[k];
      }

      return value || key;
    },
  };
}






